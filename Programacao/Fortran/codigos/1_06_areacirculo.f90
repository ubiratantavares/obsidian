!
! Programa para cálculo da área de um círculo de raio igual a 5 cm
!
PROGRAM areaCirculo

    ! Apresentaçao da finalidade do programa
    WRITE (*, *) "Cálculo da área de um círculo:"
    WRITE (*, *)
    ! Apresentaçao do comprimento do raio
    WRITE (*, *) "Raio do Círculo = ", 5, " cm"
    ! Cálculo e apresentaçao da área do círculo
    WRITE (*, *) "Área do Círculo = ", 3.14159 * 5 ** 2, " cm2"
    STOP
END PROGRAM areaCirculo
 