!
! Programa para imprimir um balão de asteriscos
!
PROGRAM cartao ! Diretiva de cabeçalho do programa

    ! Apresentação do balão
    WRITE (*, *) "    *"
    WRITE (*, *) "   ***"
    WRITE (*, *) "  * * *"
    WRITE (*, *) " *  *  *"
    WRITE (*, *) "*********"
    WRITE (*, *) " *  *  *"
    WRITE (*, *) "  * * *"
    WRITE (*, *) "   ***"
    STOP
END PROGRAM cartao ! Diretiva de fim de programa
