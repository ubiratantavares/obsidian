!
! Exercicio 1.4
!
! Calcular o valor de y na expressão y = x2 – 2x + 3, para:
!
! x = 1
!
! x = 2
!
! x = 3
!
PROGRAM exercicio_1_4

    ! Apresentação dos dados originais, realização dos cálculos e apresentação dos resultados
    WRITE (*, *) "O valor de y na expressão y = x2 – 2x + 3, para x = 1 eh ", 1**2 - 2 * 1 + 3
    WRITE (*, *) "O valor de y na expressão y = x2 – 2x + 3, para x = 2 eh ", 2**2 - 2 * 2 + 3
    WRITE (*, *) "O valor de y na expressão y = x2 – 2x + 3, para x = 3 eh ", 3**2 - 2 * 3 + 3
    STOP
END PROGRAM exercicio_1_4
