!
! Calcular o volume de um cilindro cujo raio da base é 3 centímetros e a altura 8 centímetros.
!
PROGRAM exercicio_1_2

    ! Cálculo do volume do cilindro e apresentação dos resultados
    WRITE (*, *) "O volume do cilindro cujo raio da base eh 3 cm e a altura 8 cm eh ", 3.14159 * 3**2 * 8, " centimetros cubicos"
    STOP
END PROGRAM exercicio_1_2
